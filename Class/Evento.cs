namespace ChatBot_Calendar.Class
{
    public class Evento
    {
        public string Fecha { get; set; } //Get and send the date of the event.
        public string Titulo { get; set; } //Get and send the title of the event.
        public string Descripcion { get; set; } //Get and send the description of the event.
        public string Lugar { get; set; } //Get and send the place of the event.
        public string Hora { get; set; } //Get and send the hour of the event.
        public string Comentarios { get; set; } //Get and send the comments of the event.
    }
}